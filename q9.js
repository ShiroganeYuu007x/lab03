/**
 * ฟังก์ชันจัดเรียงตัวเลขในอาร์เรย์จากน้อยไปมาก
 * @param {number[]} arr - อาร์เรย์ของตัวเลขที่ต้องการจัดเรียง
 * @returns {number[]} อาร์เรย์ที่ได้รับการจัดเรียงแล้ว
 */
function sortArray(arr) {
  // ใช้เมธอด sort() เพื่อจัดเรียงอาร์เรย์
  // หมายเหตุ: sort() ปรับเปลี่ยนอาร์เรย์ต้นฉบับและคืนค่าอาร์เรย์ที่จัดเรียง

  // วิธี 1: ใช้ spread operator (...) เพื่อสร้างอาร์เรย์ใหม่ (ไม่ปรับเปลี่ยนต้นฉบับ)
  // [...arr] สร้างสำเนาของอาร์เรย์
  return [...arr].sort((a, b) => a - b);

  // อธิบาย: (a, b) => a - b
  // - ถ้า a - b < 0 แสดงว่า a น้อยกว่า b ให้เรียง a ก่อน
  // - ถ้า a - b = 0 แสดงว่า a เท่ากับ b ไม่มีการเปลี่ยน
  // - ถ้า a - b > 0 แสดงว่า a มากกว่า b ให้เรียง b ก่อน
}

// ตัวอย่างการใช้งาน
console.log(sortArray([5, 2, 9, 1])); // => [1, 2, 5, 9]
console.log(sortArray([3, 7, 2, 9, 5])); // => [2, 3, 5, 7, 9]
console.log(sortArray([10, 1, 5, 3])); // => [1, 3, 5, 10]
console.log(sortArray([1])); // => [1] (อาร์เรย์เดียว)
console.log(sortArray([])); // => [] (อาร์เรย์ว่าง)
console.log(sortArray([9, 8, 7, 6, 5])); // => [5, 6, 7, 8, 9] (อาร์เรย์เรียงย้อนกลับ)

// ตัวอย่างเพิ่มเติม: การเปรียบเทียบต้นฉบับและผลลัพธ์
const originalArray = [5, 2, 9, 1];
const sortedArray = sortArray(originalArray);
console.log('\nอาร์เรย์ต้นฉบับ:', originalArray); // => [5, 2, 9, 1] (ไม่เปลี่ยน)
console.log('อาร์เรย์ที่จัดเรียง:', sortedArray); // => [1, 2, 5, 9]

