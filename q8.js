/**
 * ฟังก์ชัน FizzBuzz
 * พิมพ์ตัวเลข 1 ถึง n โดยมีเงื่อนไขพิเศษ:
 * - หารด้วย 3 ลงตัว → "Fizz"
 * - หารด้วย 5 ลงตัว → "Buzz"
 * - หารด้วย 3 และ 5 ลงตัว → "FizzBuzz"
 * - ไม่หารลงตัว → พิมพ์ตัวเลข
 * @param {number} n - ตัวเลขสูงสุดที่จะวนลูป
 */
function fizzBuzz(n) {
  // วนลูปจาก 1 ถึง n
  // i = 1 เป็นจำนวนแรก
  // i <= n เป็นเงื่อนไขให้วนลูปตราบเท่าที่ i ยังน้อยกว่าหรือเท่ากับ n
  // i++ เพิ่มค่า i ทีละ 1 ในแต่ละรอบ
  for (let i = 1; i <= n; i++) {
    // ตรวจสอบหารด้วย 3 และ 5 ลงตัวก่อน (% คือตัวดำเนินการหาเศษ)
    // i % 3 === 0 หมายความว่า i หารด้วย 3 ลงตัว (เศษ = 0)
    // i % 5 === 0 หมายความว่า i หารด้วย 5 ลงตัว (เศษ = 0)
    // && หมายความว่า "และ" (ต้องเป็นจริงทั้งสองเงื่อนไข)
    if (i % 3 === 0 && i % 5 === 0) {
      // ถ้าหารด้วย 3 และ 5 ลงตัว ให้พิมพ์ "FizzBuzz"
      console.log('FizzBuzz');
    }
    // ตรวจสอบหารด้วย 3 ลงตัว
    else if (i % 3 === 0) {
      // ถ้าหารด้วย 3 ลงตัว ให้พิมพ์ "Fizz"
      console.log('Fizz');
    }
    // ตรวจสอบหารด้วย 5 ลงตัว
    else if (i % 5 === 0) {
      // ถ้าหารด้วย 5 ลงตัว ให้พิมพ์ "Buzz"
      console.log('Buzz');
    }
    // ถ้าไม่เข้าเงื่อนไขข้างต้น
    else {
      // ให้พิมพ์ตัวเลข
      console.log(i);
    }
  }
}

// ตัวอย่างการใช้งาน
console.log('fizzBuzz(5):');
fizzBuzz(5);

console.log('\nfizzBuzz(15):');
fizzBuzz(15);

console.log('\nfizzBuzz(20):');
fizzBuzz(20);

