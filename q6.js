/**
 * ฟังก์ชันหาตัวเลขที่มากที่สุดในอาร์เรย์
 * @param {number[]} arr - อาร์เรย์ของตัวเลข
 * @returns {number} ตัวเลขที่มากที่สุดในอาร์เรย์
 */
function findMax(arr) {
  // ตรวจสอบว่าอาร์เรย์ว่างหรือไม่
  if (arr.length === 0) {
    return null; // ถ้าอาร์เรย์ว่าง ให้คืนค่า null
  }

  // สร้างตัวแปร max เพื่อเก็บค่าตัวเลขที่มากที่สุด
  // เริ่มต้นด้วยตัวเลขตัวแรกในอาร์เรย์ (arr[0])
  let max = arr[0];

  // วนลูปผ่านอาร์เรย์เริ่มตั้งแต่ตัวที่ 2 (i = 1)
  // เพราะเราใช้ตัวแรกเป็นค่าเริ่มต้นแล้ว
  for (let i = 1; i < arr.length; i++) {
    // เปรียบเทียบ: ถ้าตัวเลขที่ตำแหน่ง i มีค่ามากกว่า max
    if (arr[i] > max) {
      // ให้อัปเดตค่า max ให้เป็นตัวเลขตัวนี้
      max = arr[i];
    }
  }

  // คืนค่าตัวเลขที่มากที่สุด
  return max;
}

// ตัวอย่างการใช้งาน
console.log(findMax([3, 7, 2, 9, 5])); // => 9
console.log(findMax([1, 2, 3, 4, 5])); // => 5
console.log(findMax([100, 50, 75, 25])); // => 100
console.log(findMax([10])); // => 10
console.log(findMax([-5, -2, -10, -1])); // => -1 (ตัวเลขลบ)
console.log(findMax([])); // => null (อาร์เรย์ว่าง)

