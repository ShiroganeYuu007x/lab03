// /**
//  * ฟังก์ชันหาผลรวมของตัวเลขในอาร์เรย์
//  * @param {number[]} arr - อาร์เรย์ของตัวเลข
//  * @returns {number} ผลรวมของตัวเลขทั้งหมดในอาร์เรย์
//  */
function sumArray(arr) {
  // สร้างตัวแปร sum เพื่อเก็บผลรวม เริ่มต้นที่ 0
  let sum = 0;

  // วนลูปผ่านแต่ละตัวเลขในอาร์เรย์
  // i = 0 เป็นดัชนีเริ่มต้น
  // i < arr.length เป็นเงื่อนไขให้วนลูปตราบเท่าที่ i ยังน้อยกว่าความยาวของอาร์เรย์
  // i++ เพิ่มค่า i ทีละ 1 ในแต่ละรอบ
  for (let i = 0; i < arr.length; i++) {
    // เพิ่มค่าของตัวเลขที่ตำแหน่ง i ให้กับ sum
    // arr[i] คือตัวเลขที่ตำแหน่งที่ i ในอาร์เรย์
    sum += arr[i];
  }

  // คืนค่าผลรวมที่ได้
  return sum;
}

// ตัวอย่างการใช้งาน
console.log(sumArray([1, 2, 3, 4])); // => 10
console.log(sumArray([5, 10, 15])); // => 30
console.log(sumArray([100])); // => 100
console.log(sumArray([])); // => 0

